package Values;

public class TB_Book{
	private String b_id;
	private String b_name;
	private String b_author;
	private String b_img;
	private String b_press;
	private String b_pubdate;
	private int b_stock;
	
	public String getB_id() {
		return b_id;
	}
	public void setB_id(String b_id) {
		this.b_id = b_id;
	}
	public String getB_name() {
		return b_name;
	}
	public void setB_name(String b_name) {
		this.b_name = b_name;
	}
	public String getB_author() {
		return b_author;
	}
	public void setB_author(String b_author) {
		this.b_author = b_author;
	}
	public String getB_img() {
		return b_img;
	}
	public void setB_img(String b_img) {
		this.b_img = b_img;
	}
	public String getB_press() {
		return b_press;
	}
	public void setB_press(String b_press) {
		this.b_press = b_press;
	}
	public String getB_pubdate() {
		return b_pubdate;
	}
	public void setB_pubdate(String b_pubdate) {
		this.b_pubdate = b_pubdate;
	}
	public int getB_stock() {
		return b_stock;
	}
	public void setB_stock(int b_stock) {
		this.b_stock = b_stock;
	}
	
}
